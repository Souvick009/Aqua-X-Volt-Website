
function drop_0() {
    let x = document.getElementsByClassName('show-0');
    let head = document.getElementsByClassName('role_0');
    for (i = 0; i < x.length; i++) {
        if (x[i].style.display === "none") {
            x[i].style.display = "flex"
            for (i = 0; i < head.length; i++) {
                head[i].classList.toggle("glow")
            }
        }
        else {
            x[i].style.display = "none"
            for (i = 0; i < head.length; i++) {
                head[i].classList.replace("glow" , "no_glow")
            }
        }
    }
}

